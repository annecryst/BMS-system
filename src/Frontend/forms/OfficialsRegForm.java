    private void initSearchBarForOfficials(){
        ResultSet result = DB_FETCHER.fetchAllResidents();
        if (result == null) return;
        List<String> suggestions = new ArrayList<>();
        try {
            while (result.next()) {
                int    id  = result.getInt("resident_id");
                String fn  = result.getString("first_name");
                String mn  = result.getString("middle_name");
                String ln  = result.getString("last_name");
                String fullName = (fn + (mn != null && !mn.isEmpty() ? " " + mn : "") + " " + ln).trim();
                if (!fullName.isEmpty()) {
                    suggestions.add(fullName);
                    residentIdMap.put(fullName, id);
                }
            }
            officialNameDropdown.setItems(suggestions);
        } catch (SQLException ex) {
            Notifications.getInstance().show(Notifications.Type.WARNING, "Error loading residents.");
        }
    }

    private int getSelectedResidentId() {
        String selected = officialNameDropdown.getText().trim();
        return residentIdMap.getOrDefault(selected, -1);
    }

    public void populateForEdit(int officialId) {
        this.editOfficialId = officialId;
        java.sql.ResultSet rs = Backend.DB_FETCHER.fetchOfficialById(officialId);
        if (rs == null) return;
        try {
            if (rs.next()) {
                int    residentId   = rs.getInt("official_resedential_id");
                String position     = rs.getString("position");
                java.sql.Date tStart = rs.getDate("term_start");
                java.sql.Date tEnd   = rs.getDate("term_end");
                String profileImage = rs.getString("profile_image");

                java.sql.ResultSet resData = Backend.DB_FETCHER.getOfficialsResidentialInfo(residentId);
                if (resData != null && resData.next()) {
                    String fn = resData.getString("first_name");
                    String mn = resData.getString("middle_name");
                    String ln = resData.getString("last_name");
                    String fullName = (fn + (mn != null && !mn.isEmpty() ? " " + mn : "") + " " + ln).trim();
                    officialNameDropdown.setText(fullName);
                }

                posCB.setSelectedItem(position);
                if (tStart != null) termstartTF.setDate(tStart.toLocalDate());
                if (tEnd   != null) termendTF.setDate(tEnd.toLocalDate());
                if (profileImage != null && !profileImage.isEmpty()) uploadimagePL.loadFromStoredPath(profileImage);
            }
        } catch (SQLException ex) {
            Notifications.getInstance().show(Notifications.Type.WARNING, "Error loading official data.");
        }
    }
    
