    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        int residentId = getSelectedResidentId();
        
        if (residentId == -1) {
            Notifications.getInstance().show(Notifications.Type.WARNING, "Officials must be a resident.");
            return;
        }

        String position     = posCB.getSelectedItem() != null ? posCB.getSelectedItem() : "";
        java.time.LocalDate termStartDate = termstartTF.getDate();
        java.time.LocalDate termEndDate   = termendTF.getDate();
        String profileImage = uploadimagePL.getStoredFilePath() != null ? uploadimagePL.getStoredFilePath() : "";
        
        if (position.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, "Please select a position.");
            return;
        }
        if (termStartDate == null) {
            Notifications.getInstance().show(Notifications.Type.WARNING, "Please select a term start date.");
            return;
        }
        if (termEndDate == null) {
            Notifications.getInstance().show(Notifications.Type.WARNING, "Please select a term end date.");
            return;
        }

        java.sql.Date sqlTermStart = java.sql.Date.valueOf(termStartDate);
        java.sql.Date sqlTermEnd   = java.sql.Date.valueOf(termEndDate);

        if (editOfficialId == -1) {
            officials.receivedDataFromTheForm(profileImage, residentId, position, sqlTermStart, sqlTermEnd, "closed");
        } else {
            int result = Backend.DB_INSERT.updateOfficial(editOfficialId, profileImage, residentId, position, sqlTermStart, sqlTermEnd);
            Notifications.Type type = (result == 0) ? Notifications.Type.INFO : Notifications.Type.SUCCESS;
            String msg  = (result == 0) ? "Something went wrong while updating" : "Official updated successfully";
            Notifications.getInstance().show(type, msg);
            officials.refreshTable();
        }
        this.dispose();
    }//GEN-LAST:event_saveBtnActionPerformed

