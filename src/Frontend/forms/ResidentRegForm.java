    private int residentID=0;
    public void populateForEdit(int residentId) {
        this.residentID = residentId;
        buttonState = "Edit";
        try {
            ResultSet rs = DB_FETCHER.fetchResidentByID(residentId);
            if (rs == null || !rs.next()) return;

            nameTF.setText(rs.getString("first_name"));
            middleNameTF.setText(rs.getString("middle_name") != null ? rs.getString("middle_name") : "");
            lastNameTF.setText(rs.getString("last_name"));

            String sex = rs.getString("sex");
            if (sex != null) genderCB.setSelectedItem(
                    sex.substring(0, 1).toUpperCase() + sex.substring(1).toLowerCase());

            java.sql.Date bd = rs.getDate("birth_date");
            if (bd != null) BdateTF.setDate(bd.toLocalDate());

            civilStatTF.setText(rs.getString("civil_status")  != null ? rs.getString("civil_status")  : "");
            nationalityTF.setText(rs.getString("nationality") != null ? rs.getString("nationality")   : "");
            addressTF.setText(rs.getString("address")         != null ? rs.getString("address")        : "");
            purokTF.setText(rs.getString("purok")             != null ? rs.getString("purok")          : "");
            lsTF.setText(rs.getString("length_of_stay")       != null ? rs.getString("length_of_stay") : "");

            String status = rs.getString("status");
            if (status != null) statCM.setSelectedItem(status);

            String imgPath = rs.getString("profile_image");
            if (imgPath != null && !imgPath.isEmpty()) uploadimagePL.loadFromStoredPath(imgPath);

        } catch (SQLException ex) {
            System.out.println("ERROR populating form: " + ex.getMessage());
        }
    }

