    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String firstName    = nameTF.getText().trim();
        String middleName   = middleNameTF.getText().trim();
        String lastName     = lastNameTF.getText().trim();
        String sex          = genderCB.getSelectedItem() != null ? genderCB.getSelectedItem() : "";
        java.time.LocalDate dateVal = BdateTF.getDate();
        String birthDate    = dateVal != null
                              ? dateVal.format(java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd"))
                              : "";
        String civilStatus  = civilStatTF.getText().trim();
        String nationality  = nationalityTF.getText().trim();
        String address      = addressTF.getText().trim();
        String purok        = purokTF.getText().trim();
        String lengthOfStay = lsTF.getText().trim();
        String profileImage = uploadimagePL.getStoredFilePath() != null ? uploadimagePL.getStoredFilePath() : "";
        String status       = statCM.getSelectedItem() != null ? statCM.getSelectedItem() : "";

        if (firstName.isEmpty() || lastName.isEmpty() || sex.isEmpty()
                || birthDate.isEmpty() || civilStatus.isEmpty()
                || nationality.isEmpty() || address.isEmpty()
                || purok.isEmpty() || status.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Please fill in all required fields and select a birthdate.",
                "Validation Error",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(buttonState.equals("Registration")) res.receivedDataFromTheForm(
                firstName, middleName, lastName, sex, birthDate, civilStatus, nationality, address, purok, lengthOfStay, profileImage, status, "closed"
        );
        else res.UpdateTableFromEdit(
                residentID, firstName, middleName, lastName, sex, birthDate, civilStatus, nationality, address, purok, lengthOfStay, profileImage, status, "closed"
        );
        
        this.dispose();
    }//GEN-LAST:event_saveBtnActionPerformed

